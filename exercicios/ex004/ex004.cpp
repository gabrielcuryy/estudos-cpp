#include <iostream>
using namespace std;

int main() {
    //Variaveis em c++

    int intNum = 5; //Para numeros inteiros
    cout << intNum << endl;

    double flutuNum = 5.5; //Para decimais
    cout << flutuNum << endl;

    char caracVar = 'A'; //Para caracteres
    cout << caracVar << endl;

    string nome = "Gabriel"; //Para textos
    cout << nome << endl;

    bool booleano = true; //True ou False
    cout << booleano << endl;

    int idade = 20;
    cout << "Eu tenho " << idade << " anos de idade!" << endl;

    double x = 20.53;
    int y = 10;
    double soma = x + y;
    cout << soma;

    return 0;
}
